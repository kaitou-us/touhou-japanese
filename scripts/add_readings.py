def get_reading_from_jisho(word):
    """从 Jisho.org API 获取单词读音"""
    try:
        encoded_word = urllib.parse.quote(word)
        url = f'https://jisho.org/api/v1/search/words?keyword={encoded_word}'
        
        req = urllib.request.Request(url, headers={
            'User-Agent': 'Touhou-Japanese-Learning/1.0'
        })
        
        with urllib.request.urlopen(req, timeout=10) as response:
            data = json.loads(response.read().decode('utf-8'))
        
        if data.get('data') and len(data['data']) > 0:
            japanese = data['data'][0].get('japanese', [])
            if japanese and len(japanese) > 0:
                # 优先取 reading，其次取 word
                reading = japanese[0].get('reading', '')
                if not reading:
                    reading = japanese[0].get('word', '')
                return reading
        
        return ''
    except Exception as e:
        print(f'    ⚠️  查询 "{word}" 失败: {str(e)[:50]}')
        return ''


