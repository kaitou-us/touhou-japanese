def main():
    print('📝 词汇读音补全工具')
    print('=' * 60)
    
    # 加载数据
    with open(CARDS_FILE, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    cards = data.get('vocabulary_cards', [])
    missing_count = sum(1 for c in cards if not c.get('reading'))
    
    print(f'📊 词汇总量: {len(cards)}')
    print(f'📊 缺少读音: {missing_count}')
    
    if missing_count == 0:
        print('✅ 所有词汇已有读音！')
        return
    
    print(f'\n🔄 开始补全读音...')
    
    filled = 0
    api_filled = 0
    builtin_filled = 0
    
    for i, card in enumerate(cards):
        if card.get('reading'):
            continue
        
        word = card.get('word', '')
        if not word:
            continue
        
        # 先尝试内建字典
        if word in BUILTIN_READINGS:
            card['reading'] = BUILTIN_READINGS[word]
            builtin_filled += 1
            filled += 1
            continue
        
        # 如果单词本身就是假名，直接用作读音
        # 判断是否全是假名（平假名/片假名）
        is_kana = all(
            ('\u3040' <= c <= '\u309f') or ('\u30a0' <= c <= '\u30ff') or c in 'ー・'
            for c in word
        )
        if is_kana:
            card['reading'] = word
            filled += 1
            continue
        
        # 尝试 API（限速：每秒1个请求）
        if api_filled < 50:  # 限制 API 调用次数
            reading = get_reading_from_jisho(word)
            if reading:
                card['reading'] = reading
                api_filled += 1
                filled += 1
                print(f'  🌐 API: {word} → {reading}')
                time.sleep(1)  # 避免请求过快
        
        # 进度提示
        if (i + 1) % 50 == 0:
            print(f'  进度: {i + 1}/{len(cards)}, 已补全: {filled}')
    
    # 保存
    with open(CARDS_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    remaining = sum(1 for c in cards if not c.get('reading'))
    
    print(f'\n{"=" * 60}')
    print(f'📊 补全结果:')
    print(f'  内建字典: {builtin_filled} 词')
    print(f'  API 查询: {api_filled} 词')
    print(f'  假名自填: {filled - builtin_filled - api_filled} 词')
    print(f'  总计补全: {filled} 词')
    print(f'  仍缺读音: {remaining} 词')
    print(f'\n✅ 完成！文件已保存')


if __name__ == '__main__':
    main()